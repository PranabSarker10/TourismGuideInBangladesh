                   <p></p>
                  Patenga Beach is one of the renowned beaches in Bangladesh. For watching sunset, Patenga is a beautiful sea beach close to Chittagong city. It is near the beginning of the Karnaphuli River. Nature lovers come here to enjoy the beautiful scenario of the Bay of Bengal and get away from busy city life. Patenga is a popular tourist spot. The beach is very close to Bangladesh Naval Academy of Bangladesh Navy, and Shah Amanat International Airport. The width of the beach is narrow and swimming is not recommended in the seas here.
                   <p></p>
                   As part of the sea shore a concrete walls can be seen along the shore. Also large blocks of stones have been laid out to prevent erosion. During 1990s lots of restaurants and kiosks have been developed around the beach area. Lighting of the beach area has enhanced the security aspect of night visit. People visiting this beautiful place usually wake up early to witness the stunning sunrise.
