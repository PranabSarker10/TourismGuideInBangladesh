                   Zainul Abedin Sangrahashala (alternate: Shilpacharya Zainul Abedin Sangrahashala; translation: "Great Teacher of the Arts" Zainul Abedin "Art Gallery") is an art museum in Mymensingh, Bangladesh. Established in 1975, it contains the collections of the artist Shilpacharya Zainul Abedin (1914 to 1976). The art gallery was established in Mymensingh as this is where the artist spent his early days. Abedin, a pioneer of the country's modern art movement, created works of art on subject matters such as the Bengal famine of 1943 and the peoples' independence.The museum is located in the area of Shaheeb Quarter Park on the bank of the Old Bramaputra River.     
