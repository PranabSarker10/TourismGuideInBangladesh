                   <li>Shalbon Bihar, Mainamati, Comilla ()</li>   
                   <img src="img/Shalbon Bihar.jpg"height=440px width=850px> 
                   <p></p>
                   The archeological site, Mainamati is located 8 miles from Comilla town. There are a series of hillocks where the northern part is called Mainamati; the southern part is called Lalmai and at the mid of this part contains Shalbon Bihar established by the King Buddha Dev in the 8th century.
                    <p></p>
                   <em><b>Tourist attractions:</b></em>
                   <ul>
                       <li> Shalbon Bihar consists of 115 cells with large courtyard room with a cruciform temple at the center. It was Vihara means educational centre with residential facilities. </li>
                       <li> Kotila Mura is 5km north of Shalban Bihar where three stupas are found representing Buddhist 'Trinity' which means three Jewels- Buddha, Dharma and Sangha. </li>
                       <li> Charpatra Mura, an isolated small oblong shrine is located about 2.5 km northwest of Kotila Mura. </li>
                       <li> Visit Mainamati Museum which is enriched with collections of copper plates, 86 bronze objects, gold and silver coins, etc. You'll find there 150 bronze statues restored from bronze stupas, monastic cells and stone sculptures. </li>    
                       <p></p>
                   </ul>
