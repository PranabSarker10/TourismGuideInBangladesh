                   <img src="img/Coxs Bazar.jpg"height=440px width=850px>
                   <p></p>
                   Cox's Bazar, the longest sea beach (120 km long) in the world, is located in the eastern coastline of Bangladesh. It's declared as one of the Seven Wonders of the World.
                   <p></p>
                   <em><b>Tourist attractions:</b></em>
                   <ul>
                       <li> The vast sky and sea merge to let you know about their unity. You can enjoy the fiery sun rising from the sea and again sinking  under the sea in the evening. The beach sparkles like the pearl when light falls on the sand. </li>
