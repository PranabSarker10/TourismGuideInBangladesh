                   <p></p>
                   Bangladesh Agricultural University or BAU was established as the only university of its kind in Bangladesh in 1961. The scheme for BAU was finalised on 8 June 1961 and its ordinance was promulgated on 18 August 1961. It started functioning with the College of Animal Husbandry and Veterinary Science at Mymensingh as its nucleus. The university has six faculties and 43 departments covering all aspects of agricultural education and research.
                   <p></p>
                   BAU was the second highest budgeted public university in Bangladesh for the year 2013–2014. BAU's unparalleled research in agriculture has made it well recognised across Asia. Having a very low teacher-student ratio, the quality of education at BAU is remarkable.
