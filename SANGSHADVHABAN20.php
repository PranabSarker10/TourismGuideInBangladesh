                   Jatiya Sangsad Bhaban or National Parliament House, is the house of the Parliament of Bangladesh, located at Sher-e-Bangla Nagar in the Bangladeshi capital of Dhaka. Designed by architect Louis Kahn, the complex is one of the largest legislative complexes in the world, comprising 200 acres (800,000 m²)
                   <p></p>
                   The building was featured prominently in the 2003 film My Architect, detailing the career and familial legacy of its architect, Louis Kahn. Robert McCarter, author of Louis I. Kahn, described the National Parliament of Bangladesh as one of the twentieth century's most significant buildings.
