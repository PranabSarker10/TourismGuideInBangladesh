                   <em><b>Things to do:</b></em>
                     <P></P>
                     Madhabkunda is surrounded by lush tea estates and is full of water lilies. They make an enchanting combination with the largest waterfall in Bangladesh. Every year, thousands of tourists are drawn to Madhabkunda because of its natural beauty, especially in winter (Nov-Feb), when they come for picnics or longer pleasure trips. Most of the people visit the falls during winter season and this is the only season that you can go to top of the waterfall. During the rainy season, the amount of water is massive and multiple water streams can be seen. Bathing is not permitted as it is risky to get closer to the falls.
