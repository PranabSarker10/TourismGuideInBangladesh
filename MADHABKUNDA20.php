                   Madhabkunda Waterfall and Eco Park is situated in Barlekha Upazila in Moulvibazar District of Sylhet Division. It is one of the largest waterfalls in Bangladesh. The waterfall is a popular tourist spot in Bangladesh. Big boulders, surrounding forest, and the adjoining streams attract many tourists for picnic parties and day trips.
                   <p></p>
                   The 267 hectors Madhabkunda Eco Park (est. 2001) is located at Madhabkunda under Moulvibazar District. This area was declared as Eco Park with a view to conserve the Madhabkunda waterfall and surrounding biodiversity. The Madhabkunda Waterfall is the most attractive site of this Eco Park which is about 200 ft. (61 m) high. A section of 'Khasia' tribe lives in the forest villages of this area. They are engaged in some agro-economic activities in the forestland.
