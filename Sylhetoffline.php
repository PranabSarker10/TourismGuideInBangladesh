                   <li>Ratargul,sylhet() </li>
                   <img src="img/Ratargul.jpg"height=440px width=850px> 
                   <p></p>
                   Ratargul, the only swamp forest in Bangladesh is located in Gowainghat upazila of Sylhet. The beauty is bloomed mainly in rainy season.         <p></p>
                   In gwainghat hire boat and heads towards island like place from where again hire a small boat for ratargul forest. Here it will cost BDT 200-500 for exploring ratargul forest. It's better to hire traditional boat instead of engine boat where it's shakiness will give you real experience of the boat riding.
                   <p></p>
                   <em><b>Tourist attractions:</b></em>
                   <p></p>
                   <ul>
                       <li> Most of the trees here are 'koroch' trees, a spot of attraction for the botanist. The reflection of the koroch tree on the crystal clear water of Chengir Khal creates mesmerizing ambiance. </li>
                       <li> Freshwater swamp forest is a good source of local fishes during rainy season. Besides, monkeys, lizards, birds insects, green spiders, birds etc are available to offer you flavor of Amazon forest. </li>
                       <li> Ratargul will provide you very adventurous and scary feeling. During boat riding the low hanging branches will hit you slightly and again you may stuck in the narrow gap between the trees what make your ride more daring. </li>
                       <li> Monsoon, in the month of July and August is the best time for visiting ratargul which gets submerged under 20 to 30 feet water in some part and rest of the year the water level is 10 feet. </li>
                       Report and take permission from the forest office for exploring Ratargul and they'll manage boat for you. Don't hire from outside as there might be the possibility of be hijacked. Don't throw anything here and there and don't destroy the nature.
                   </ul>              
