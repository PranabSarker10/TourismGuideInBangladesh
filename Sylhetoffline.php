                   <p></p>
                   <ul>
                       <li> Crystal clear water, mesmerizing ambiance and the the whiteness of the stones is definitely going to bewitch you. You can also take your shower here with cold and soothing water. But if you can't swim then avoid swimming here. </li>
                       <li> You'll astonish to see the bonding of the hills, clouds, white stones and the depth of the river. </li>
                       <li> Cold melodious sound of water flow, sunrise, sunset and moonlit night will soothe your body and mind and make you lively and fresh. </li>
