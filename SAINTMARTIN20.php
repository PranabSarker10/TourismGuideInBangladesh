                   <em><b>Description:</b></em>
                   <p></p>
                   St. Martin's Island in the northeastern part of the Bay of Bengal, about 9 km south of the tip of the Cox's Bazar-Teknaf peninsula, and forming the southernmost part of Bangladesh. Legend has it that in the early part of the seventh century an Arabian merchant ship fully loaded with cinnamons was hit hard by a huge submarine rock near an atoll during a foul weather and was broken asunder scattering cinnamon sticks all over the island, thus giving birth to the name of "Daruchinir Deep" meaning the Island of Cinnamons. But local people used to call this landmass Narikel Jinjira, which means the Island of Coconuts, as the island was once fringed with dense groves of coconut palms. During cadastral survey of India in the early part of the last century surveying authority named this coral island as Saint Martin's Island probably after the name of a British saint.
                   <p></p>
                   More than 3000 people arrive at the Saint Martin Island every morning and leave the island the same day by 3 in the afternoon to splurge the daytime to soak up sun-rays and hang around in solitary seclusion on the sea beach surrounded by corals and clear blue water of the sea. Panoramic beauty and the feelings of being amidst pristine marine life attracts many of the visitors to stay there overnight to enjoy sensing and sighting the beauties of darkness with no electric lights around blurring vision. Laying back on a beach chair, gazing at the nocturnal sky studded with a dazzling moon or billions of gorgeous stars, listening to undulating sonic sounds of sea waves, and reflecting on the mysteries of creations, when time is on your side, is an experience that needs to be felt only, not described.
